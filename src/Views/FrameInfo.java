    /**
     * Cambia la ImageIcon delle label jL_CharLeft, jL_Middle, jL_CharRight
     * @param value -1 = spostamente verso sinistra / 0 = set on middle / 1 = spostamento verso destra
     */
   private void setLabel(int value) {
        
        this.fModel.createNewFont(25f);
        jL_Description.setFont(this.fModel.getFont());
        jL_Description.setText("");
        
        JPa_Btn.setVisible(true);
        switch (this.whatClicked) {
            case 1 -> {
                jPanel1.requestFocus();
                switch(value) {
                    case 1:
                        if(this.posVettoreE+1 < this.vetChar.length-1){
                            this.posVettoreE++;
                            setLabelEntity();
                        }   break;
                    case -1:
                        if(this.posVettoreE-1 > 0){
                            this.posVettoreE--;
                            setLabelEntity();
                        }   break;
                    case 0:
                        setLabelEntity();
                        break;
                }
            }
            case 2 -> {
                jPanel1.requestFocus();
                switch(value) {
                    case 1:
                        if(this.posVettoreA+1 < this.vetAbility.length-1){
                            this.posVettoreA++;
                            setLabelAbility();
                        }   break;
                    case -1:
                        if(this.posVettoreA-1 > 0){
                            this.posVettoreA--;
                            setLabelAbility();
                        }   break;
                    case 0:
                        setLabelAbility();
                        break;
                }
            }
            case 3 -> {
                this.fModel.createNewFont(15f);
                JPa_Btn.setVisible(false);
                jL_Description.setFont(this.fModel.getFont());
                jL_CharLeft.setIcon(null);
                jL_Middle.setIcon(null);
                jL_CharRight.setIcon(null);
                
            }
            
        }
        setDescription();
    }

