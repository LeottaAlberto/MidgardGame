    //inizializza vettore personaggi e abilità
    private void inizVettoreChar() {
        int iter = 0;
        while(iter < this.vetChar.length){
            try{
                if(iter == 0) this.vetChar[iter] = this.charSt.empty;
                else if(iter == this.vetChar.length-1) this.vetChar[iter] = this.charSt.empty;
                else{
                    if(iter <= this.NORMAL)this.vetChar[iter] = new Normal(rifNormal(iter));
                    else this.vetChar[iter] = new Hero(rifHero(iter));
                }
            }catch (Exception e) {
                System.err.println("Errore nel caricare il vettore character. Cella: " + iter + " " + e);
                this.vetChar[iter] = new Entity(this.charSt.programmatore);
            }
            iter++;
        }
    }
    private void inizVettoreAbility() {
        int iter = 0;
        while(iter < this.vetAbility.length){
            try {
                if(iter == 0) this.vetAbility[iter] = this.abiSt.empty;
                else if(iter == this.vetAbility.length-1) this.vetAbility[iter] = this.abiSt.empty;
                else this.vetAbility[iter] = new Ability(ability(iter));
            } catch (Exception e) {
                System.err.println("Errore nel caricare il vettore ability. Cella: " + iter + " " + e);
                this.vetAbility[iter] = new Ability(this.abiSt.perfectCoding);
            }
            iter++;
        }
    }

